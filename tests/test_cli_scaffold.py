def _make_profile_dir(tmp_path: Path, dataset: str, year: int) -> Path:
    """Create a minimal raw profile in the expected location."""
    profile_dir = tmp_path / "data" / "raw" / dataset / str(year) / "_profile"
    profile_dir.mkdir(parents=True, exist_ok=True)
    profile = {
        "dataset": dataset,
        "year": year,
        "file_used": f"{dataset}.csv",
        "encoding_suggested": "utf-8",
        "delim_suggested": ";",
        "decimal_suggested": ",",
        "skip_suggested": 0,
        "robust_read_suggested": False,
        "header_line": "col1;col2;col3",
        "columns_raw": ["col1", "col2", "col3"],
        "columns_norm": ["col1", "col2", "col3"],
        "missingness_top": [],
        "sample_rows": [],
        "mapping_suggestions": {
            "col1": {"from": "col1", "type": "str"},
            "col2": {"from": "col2", "type": "int"},
            "col3": {"from": "col3", "type": "double"},
        },
        "warnings": [],
    }
    (profile_dir / "raw_profile.json").write_text(json.dumps(profile), encoding="utf-8")
    return profile_dir


def _make_dataset_yml(tmp_path: Path, dataset: str, years: list[int]) -> Path:
    """Create a minimal dataset.yml."""
    yml = tmp_path / "dataset.yml"
    years_str = ", ".join(str(y) for y in years)
    yml.write_text(
        f"root: {tmp_path}\ndataset:\n  name: {dataset}\n  years: [{years_str}]\nclean:\n  sql: sql/clean.sql\n",
        encoding="utf-8",
    )
    return yml


