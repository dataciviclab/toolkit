def test_scaffold_clean_on_project_example(tmp_path: Path):
    """Integration test: scaffold clean should work on the project-example dataset."""
    src = Path("project-example")
    dst = tmp_path / "project-example"
    shutil.copytree(src, dst)

    # Create a minimal raw CSV so profile raw can work
    raw_dir = dst / "_smoke_out" / "data" / "raw" / "project_example" / "2022"
    raw_dir.mkdir(parents=True, exist_ok=True)
    (raw_dir / "ispra_dettaglio_comunale_2022.csv").write_text(
        "comune;provincia;regione;anno;val\nRoma;RM;Lazio;2022;100\n",
        encoding="utf-8",
    )

    # First run profile raw to generate the profile
    result = runner.invoke(
        app,
        ["profile", "raw", "-c", str(dst / "dataset.yml")],
    )
    assert result.exit_code == 0

    # Now scaffold clean
    result = runner.invoke(
        app,
        ["scaffold", "clean", "-c", str(dst / "dataset.yml"), "--dry-run"],
    )
    assert result.exit_code == 0
    assert "Generated by toolkit scaffold clean" in result.output
    assert "SELECT" in result.output
