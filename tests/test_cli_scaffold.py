class TestProposeCleanRead:
    def test_with_mapping(self):
        profile = {
            "delim_suggested": ";",
            "encoding_suggested": "utf-8",
            "decimal_suggested": ",",
            "header_line": "col1;col2",
            "skip_suggested": 0,
            "mapping_suggestions": {
                "col1": {"from": "col1", "type": "str"},
                "col2": {"from": "col2", "type": "int"},
            },
        }
        result = propose_clean_read(profile)
        assert result["delim"] == ";"
        assert result["encoding"] == "utf-8"
        assert result["decimal"] == ","
        assert "columns" not in result  # header: true → no columns needed
        assert result["header"] is True  # mapping matches header → header: true
        assert result.get("skip", 0) == 0  # no skip needed with header: true

    def test_no_mapping_no_columns(self):
        profile = {
            "delim_suggested": ",",
            "header_line": "a,b",
            "skip_suggested": 0,
            "columns_raw": ["a", "b"],
            "mapping_suggestions": {},
        }
        result = propose_clean_read(profile)
        assert "columns" not in result
        assert result["header"] is True  # no explicit columns
        assert result["delim"] == ","

    def test_skip_with_header_and_preamble(self):
        """When header_line present and mapping keys match: header:true, skip preserved."""
        profile = {
            "delim_suggested": ";",
            "header_line": "col1;col2",
            "skip_suggested": 1,
            "mapping_suggestions": {
                "col1": {"from": "col1", "type": "str"},
                "col2": {"from": "col2", "type": "str"},
            },
        }
        result = propose_clean_read(profile)
        # header names match mapping keys → header:true, skip stays as-is
        assert result["header"] is True
        assert result.get("skip", 0) == 1  # skip_suggested preserved

    def test_skip_preserved_without_header(self):
        """When no header_line but skip_suggested > 0, skip is preserved."""
        profile = {
            "delim_suggested": ";",
            "skip_suggested": 3,
            "mapping_suggestions": {
                "col1": {"from": "col1", "type": "str"},
            },
        }
        result = propose_clean_read(profile)
        assert result["skip"] == 3

    def test_robust_suggested_no_mode_in_read(self):
        """robust_read_suggested does NOT produce a key inside clean.read.
        It should be set as clean.read_mode instead."""
        profile = {
            "delim_suggested": ";",
            "robust_read_suggested": True,
            "mapping_suggestions": {},
            "warnings": [],
        }
        result = propose_clean_read(profile)
        # No mode inside clean.read
        assert "mode" not in result

    def test_output_is_loadable_by_clean_read_config(self):
        """propose_clean_read output is valid for CleanReadConfig (no extra keys, correct types)."""
        from toolkit.core.config_models.clean import CleanReadConfig

        profile = {
            "delim_suggested": ",",
            "encoding_suggested": "utf-8",
            "decimal_suggested": ",",
            "header_line": "col1,col2,col3",
            "skip_suggested": 0,
            "mapping_suggestions": {
                "col1": {"from": "col1", "type": "str"},
                "col2": {"from": "col2", "type": "int"},
                "col3": {"from": "col3", "type": "double"},
            },
        }
        proposed = propose_clean_read(profile)
        # Must not raise pydantic.ValidationError
        config = CleanReadConfig(**proposed)
        assert config.delim == ","
        assert config.decimal == ","
        # header:true perché mapping matcha header_line → columns auto-detected
        assert config.header is True
        assert config.columns is None

    def test_maps_duckdb_types_correctly(self):
        profile = {
            "delim_suggested": ",",
            "mapping_suggestions": {
                "int_col": {"from": "int_col", "type": "int"},
                "float_col": {"from": "float_col", "type": "double"},
                "date_col": {"from": "date_col", "type": "date"},
                "bool_col": {"from": "bool_col", "type": "bool"},
                "str_col": {"from": "str_col", "type": "str"},
            },
        }
        result = propose_clean_read(profile)
        cols = result["columns"]
        assert cols["int_col"] == "BIGINT"
        assert cols["float_col"] == "DOUBLE"
        assert cols["date_col"] == "DATE"
        assert cols["bool_col"] == "BOOLEAN"
        assert cols["str_col"] == "VARCHAR"

    @pytest.mark.policy
    def test_header_detected_when_names_differ_from_mapping(self):
        """When header_line has real names but mapping has generic columnXX,
        propose header:true and drop the generic columns mapping.
        This is the real bug: profiler read header as data with header=false."""
        profile = {
            "delim_suggested": ";",
            "header_line": "CodiceEnte;Importo",
            "skip_suggested": 0,
            "mapping_suggestions": {
                "column01": {"from": "column01", "type": "str"},
                "column02": {"from": "column02", "type": "str"},
            },
        }
        result = propose_clean_read(profile)
        # Header has real names that differ from generic columnXX → header=true
        assert result["header"] is True
        # columns should be dropped since we now use real header
        assert "columns" not in result
        # skip stays 0 (no bump needed, header line is now recognized as header)
        assert result.get("skip", 0) == 0

    @pytest.mark.policy
    def test_header_detected_with_column_prefix_mapping(self):
        """Same as above but with 'col' prefix instead of 'column' prefix."""
        profile = {
            "delim_suggested": ";",
            "header_line": "Regione;Provincia;Comune",
            "skip_suggested": 0,
            "mapping_suggestions": {
                "col1": {"from": "col1", "type": "str"},
                "col2": {"from": "col2", "type": "str"},
                "col3": {"from": "col3", "type": "str"},
            },
        }
        result = propose_clean_read(profile)
        # "Regione" != "col1" after normalization → header detected
        assert result["header"] is True
        assert "columns" not in result

    @pytest.mark.policy
    def test_no_header_false_positive_from_numeric_first_row(self):
        """A positional CSV with first row as data (no real header) must NOT
        trigger header:true even if names differ from column01/02.
        Real header identifiers contain at least one letter."""
        profile = {
            "delim_suggested": ";",
            "header_line": "1;2;3",  # numeric data, not a real header
            "skip_suggested": 0,
            "mapping_suggestions": {
                "column01": {"from": "column01", "type": "str"},
                "column02": {"from": "column02", "type": "str"},
                "column03": {"from": "column03", "type": "str"},
            },
        }
        result = propose_clean_read(profile)
        # Numeric "1;2;3" has no letters → not treated as real header
        assert result["header"] is False
        assert result.get("columns") is not None  # columns preserved


