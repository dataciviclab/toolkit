    """Output YAML must parse correctly with special chars like comma, semicolon."""
    import yaml

    from toolkit.scaffold.clean import format_clean_read_proposal

    # Decimal comma and semicolon delimiter — these need quoting in YAML
    profile = {
        "delim_suggested": ";",
        "decimal_suggested": ",",
        "encoding_suggested": "utf-8",
        "mapping_suggestions": {},
    }
