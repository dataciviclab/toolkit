class TestGenerateCleanSql:
    def test_minimal_profile(self, tmp_path: Path):
        profile = {
            "file_used": "test.csv",
            "encoding_suggested": "utf-8",
            "delim_suggested": ";",
            "decimal_suggested": ",",
            "columns_raw": ["col1", "col2"],
            "mapping_suggestions": {},
            "warnings": [],
        }
        sql = generate_clean_sql(profile, "test_ds", 2024, tmp_path)
        assert "-- Generated by toolkit scaffold clean" in sql
        assert "SELECT" in sql
        assert "read_csv" in sql
        assert "delim=';'" in sql
        assert "encoding='utf-8'" in sql

    def test_with_mapping_suggestions(self, tmp_path: Path):
        profile = {
            "file_used": "bdap.csv",
            "encoding_suggested": "utf-8",
            "delim_suggested": ";",
            "decimal_suggested": ",",
            "header_line": "CodiceEnte;Importo",
            "skip_suggested": 0,
            "columns_raw": ["CodiceEnte", "Importo"],
            "mapping_suggestions": {
                "CodiceEnte": {"from": "CodiceEnte", "type": "str"},
                "Importo": {"from": "Importo", "type": "double"},
            },
            "warnings": [],
        }
        sql = generate_clean_sql(profile, "bdap_lea", 2024, tmp_path)
        assert "TRY_CAST" in sql
        assert "CodiceEnte" in sql
        assert "Importo" in sql
        assert "BIGINT" in sql or "DOUBLE" in sql
        assert "codice_ente" in sql
        assert "importo" in sql
        # When columns are explicit, header=false and skip bumped by 1
        assert "header=false" in sql
        assert "skip=1" in sql

    def test_header_skip_with_skip_suggested(self, tmp_path: Path):
        """When header_line exists and skip_suggested > 0 with columns, skip should be bumped."""
        profile = {
            "file_used": "test.csv",
            "encoding_suggested": "utf-8",
            "delim_suggested": ";",
            "header_line": "col1;col2",
            "skip_suggested": 1,
            "columns_raw": ["col1", "col2"],
            "mapping_suggestions": {
                "col1": {"from": "col1", "type": "str"},
                "col2": {"from": "col2", "type": "int"},
            },
            "warnings": [],
        }
        sql = generate_clean_sql(profile, "test", 2024, tmp_path)
        # header_line present + columns present -> header=false, skip=1+1=2
        assert "header=false" in sql
        assert "skip=2" in sql

    def test_no_columns_keeps_header_true(self, tmp_path: Path):
        """When truly no columns available, header stays true."""
        profile = {
            "file_used": "test.csv",
            "delim_suggested": ",",
            "header_line": "a,b",
            "skip_suggested": 0,
            "columns_raw": [],
            "mapping_suggestions": {},
            "warnings": [],
        }
        sql = generate_clean_sql(profile, "test", 2024, tmp_path)
        assert "header=true" in sql
        # With no columns, SELECT * is used
        assert "SELECT\n    *" in sql

    def test_columns_sql_is_single_line(self, tmp_path: Path):
        """Verify columns param is on same line as other params (no missing comma)."""
        profile = {
            "file_used": "test.csv",
            "delim_suggested": ";",
            "header_line": "col1;col2",
            "skip_suggested": 0,
            "columns_raw": ["col1", "col2"],
            "mapping_suggestions": {
                "col1": {"from": "col1", "type": "str"},
                "col2": {"from": "col2", "type": "int"},
            },
            "warnings": [],
        }
        sql = generate_clean_sql(profile, "test", 2024, tmp_path)
        # All params should be on the same line after read_csv(
        # Pattern: read_csv('...', ..., columns={...})
        assert "FROM read_csv(" in sql
        # The columns param should follow other params with proper comma separation
        assert "auto_detect=false, columns={" in sql


